function openpage() {
	var x = document.getElementById("indian").value.toLowerCase();
	if (x === "la cianzon de la val de fascia") {
		window.open("la_cianzon_de_la_val_de_fascia.html","_self")	
	}
	if (x === "crepe spavide") {
		window.open("crepe_spavide.html","_self")
	}
	if (x === "bel lengaz") {
		window.open("bel_lengaz.html","_self")
	}
	if (x === "oh ladinia") {
		window.open("oh_ladinia.html","_self")
	}
	if (x === "poltron polka") {
		window.open("poltron_polka.html","_self")
	}
	if (x === "le darmole") {
		window.open("le_darmole.html","_self")
	}
}
